package org.encuestame.utils.security;

/**
 * Description Class.
 * @author Picado, Juan juanATencuestame.org
 * @since Feb 5, 2011 4:44:10 PM
 */
public class ProfileUserAccount {

    private String username;

    private String email;

    private String name;

    private Boolean privateProfile;

    private String language;

    private String bio;

    /**
     * @return the username
     */
    public String getUsername() {
        return username;
    }

    /**
     * @param username the username to set
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * @return the email
     */
    public String getEmail() {
        return email;
    }

    /**
     * @param email the email to set
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * @return the privateProfile
     */
    public Boolean getPrivateProfile() {
        return privateProfile;
    }

    /**
     * @param privateProfile the privateProfile to set
     */
    public void setPrivateProfile(Boolean privateProfile) {
        this.privateProfile = privateProfile;
    }

    /**
     * @return the language
     */
    public String getLanguage() {
        return language;
    }

    /**
     * @param language the language to set
     */
    public void setLanguage(String language) {
        this.language = language;
    }

    /**
     * @return the bio
     */
    public String getBio() {
        return bio;
    }

    /**
     * @param bio the bio to set
     */
    public void setBio(String bio) {
        this.bio = bio;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /* (non-Javadoc)
     * @see java.lang.Object#toString()
     */
    @Override
    public String toString() {
        StringBuilder builder = new StringBuilder();
        builder.append("ProfileUserAccount [username=").append(username)
                .append(", email=").append(email).append(", privateProfile=")
                .append(privateProfile).append(", language=").append(language)
                .append(", bio=").append(bio).append("]");
        return builder.toString();
    }
}
